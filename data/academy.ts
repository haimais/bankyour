function pickFormulaSet(seed: LessonSeed): [FormulaSpec, FormulaSpec] {
  const haystack = `${seed.title} ${seed.summary} ${seed.tags.join(" ")}`.toLowerCase();

  if (/(вклад|депозит|капитализац|сбереж)/i.test(haystack)) {
    return [
      {
        title: "Эффективная доходность (EIR)",
        expression: "EIR = (1 + r / m)^m - 1",
        explanation: "Показывает реальную годовую доходность с учетом частоты начисления процентов."
      },
      {
        title: "Будущая стоимость вклада",
        expression: "FV = PV × (1 + r)^n",
        explanation: "Оценка итоговой суммы на горизонте n периодов при фиксированной ставке."
      }
    ];
  }

  if (/(кредит|ипотек|долг|аннуитет|рефинанс)/i.test(haystack)) {
    return [
      {
        title: "Аннуитетный платеж",
        expression: "P = S × i / (1 - (1 + i)^-n)",
        explanation: "Расчет стабильного ежемесячного платежа по кредиту при фиксированной ставке."
      },
      {
        title: "Долговая нагрузка",
        expression: "DSR = Платежи по долгам / Чистый доход",
        explanation: "Ключевой индикатор устойчивости кредитной нагрузки в личном бюджете."
      }
    ];
  }

  if (/(бизнес|рко|эквайринг|лизинг|гарант|вэд)/i.test(haystack)) {
    return [
      {
        title: "Полная стоимость сервиса (TCO)",
        expression: "TCO = Абонплата + Переменные комиссии + Скрытые издержки",
        explanation: "Позволяет сопоставить банковские сервисы не по рекламе, а по фактической стоимости владения."
      },
      {
        title: "Операционная маржа после финансовых комиссий",
        expression: "OM = (Выручка - OPEX - FinCom) / Выручка",
        explanation: "Показывает, как банковские тарифы и комиссии влияют на прибыльность бизнеса."
      }
    ];
  }

  if (/(налог|вычет|льгот|tax)/i.test(haystack)) {
    return [
      {
        title: "Налоговая база",
        expression: "Base = Доходы - Расходы - Допустимые вычеты",
        explanation: "База определяет сумму налога к уплате в рамках правил выбранного режима."
      },
      {
        title: "Эффективная налоговая ставка",
        expression: "ETR = Налог к уплате / Валовый доход",
        explanation: "Метрика показывает долю дохода, которая уходит на налоги после учета льгот."
      }
    ];
  }

  if (/(портфел|акци|облига|etf|пиф|цфа|инвест|риск|валют)/i.test(haystack)) {
    return [
      {
        title: "Ожидаемая доходность портфеля",
        expression: "E(Rp) = Σ(wi × Ri)",
        explanation: "Средневзвешенная ожидаемая доходность по долям активов портфеля."
      },
      {
        title: "Реальная доходность",
        expression: "Rreal = (1 + Rnom) / (1 + inflation) - 1",
        explanation: "Позволяет оценить рост покупательной способности после учета инфляции."
      }
    ];
  }

  return [
    {
      title: "Коэффициент выполнения финансового плана",
      expression: "Plan KPI = Факт / План",
      explanation: "Показывает дисциплину исполнения целей и динамику отклонений."
    },
    {
      title: "Темп накопления",
      expression: "Savings Rate = Накопления / Доход",
      explanation: "Базовый показатель устойчивости личной финансовой системы."
    }
  ];
}

