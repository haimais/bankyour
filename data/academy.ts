export function getAcademyModules(level?: AcademyLevel): AcademyModule[] {
  if (!level) {
    return ACADEMY_MODULES;
  }
  return ACADEMY_MODULES.filter((moduleItem) => moduleItem.level === level);
}

export function getAcademyModuleBySlug(slug: string): AcademyModule | null {
  return ACADEMY_MODULES.find((moduleItem) => moduleItem.slug === slug) ?? null;
}

